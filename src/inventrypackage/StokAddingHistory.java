    private void LoadStockHistory(){
        ResultSet SAH = Database.getData("SELECT * FROM STOCKADDINGHISTORY SAH INNER JOIN PRICEBATCH PB ON SAH.PRICEBATCHID=PB.ID INNER JOIN PRODUCTTBL PT ON PB.PRODCODE=PT.PRODCODE ");
        dtm.setRowCount(0);
        try {
            while(SAH.next()){

                Vector p = new Vector();
                p.add(SAH.getString(12));
                p.add(SAH.getString(7));
                p.add(SAH.getInt(4));
                p.add(SAH.getDouble(3));
                p.add(SAH.getString(5));
                p.add(SAH.getInt(2));
                p.add(SAH.getInt(1));

                dtm.addRow(p);

            }
        } catch (SQLException ex) {
            Logger.getLogger(Products.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
