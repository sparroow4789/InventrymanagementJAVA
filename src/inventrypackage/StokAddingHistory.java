    private void RemoveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_RemoveBtnMouseClicked
        
        if(pbid.getText().isEmpty() || pcode.getText().isEmpty() || qty.getText().isEmpty() || stockai.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Select data to be deleted.");
        }else{
            
            
            if(Database.saveData("DELETE FROM STOCKADDINGHISTORY WHERE ID = "+stockai.getText()+" ")){
                System.out.println("Only Stock History deleted successfully.");
                
                if(Database.saveData("UPDATE PRICEBATCH SET PRODQTY = PRODQTY - "+qty.getText()+" WHERE ID = "+pbid.getText()+" ")){
                    System.out.println("Only Update priceBatch deleted successfully.");
                
                    if(Database.saveData("UPDATE PRODUCTTBL SET PRODQTY = PRODQTY - "+qty.getText()+" WHERE PRODCODE='"+pcode.getText()+"'")){
                        JOptionPane.showMessageDialog(rootPane, "Stock deleted successfully.");
                        System.out.println("Stock deleted successfully.");
                        
                        LoadStockHistory();

                        qty.setText("");
                        pbid.setText("");
                        pcode.setText("");
                        stockai.setText("");
                        
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "Failed, ERROR CODE: SPRITE");
                        System.out.println("Failed, ERROR CODE: SPRITE");
                    }    
                    
                
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Failed, ERROR CODE: COKE");
                    System.out.println("Failed, ERROR CODE: COKE");
                }
                
                
                
                
                
            }else{
                    JOptionPane.showMessageDialog(rootPane, "Failed, ERROR CODE: SNAKE");
                    System.out.println("Failed, ERROR CODE: SNAKE");
                }
            
        }
        
        
        
    }//GEN-LAST:event_RemoveBtnMouseClicked

